char	*ft_strcpy(char *dest, char *src)
{
	char	*saved_dest;

	saved_dest = dest;
	while (*src != '\0')
	{
		*dest = *src;
		dest++;
		src++;
	}
	*dest = '\0';
	return (saved_dest);
}
