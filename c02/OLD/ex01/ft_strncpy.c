char	*ft_strncpy(char *dest, char *src, unsigned int n)
{
	char	*saved_dest;

	saved_dest = dest;
	while (n > 0 && *src != '\0')
	{
		*dest = *src;
		dest++;
		src++;
		n--;
	}
	while (n > 0)
	{
		*dest = '\0';
		dest++;
		n--;
	}
	return (saved_dest);
}
